CREATE TABLE ORDER_DATA
(
	 --ORDERID INT NOT NULL IDENTITY(1,1)
	 ORDERID INT NOT NULL
	,ORDERDATE VARCHAR(12)
	,CUSTOMERID INT
	,AMOUNT NUMERIC(6,2) NOT NULL
	,CONSTRAINT PK_ORDERID PRIMARY KEY (ORDERID)
)
