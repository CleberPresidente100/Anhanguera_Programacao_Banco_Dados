CREATE TABLE LINEITEM
(
	 --ORDERLINE INT NOT NULL IDENTITY(1,1)
	 ORDERLINE INT NOT NULL
	,ORDERID INT NOT NULL
	,PRODUCTID INT NOT NULL
	,QUANTITY NUMERIC(4,0) NOT NULL
	,CONSTRAINT PK_ORDERLINE PRIMARY KEY (ORDERLINE)
)
GO


CREATE TABLE CUSTOMER
(
	 --CUSTOMERID INT NOT NULL IDENTITY(1,1)
	 CUSTOMERID INT NOT NULL
	,[NAME] VARCHAR(255)
	,CONSTRAINT PK_CUSTOMERID PRIMARY KEY (CUSTOMERID)
)
GO


CREATE TABLE PRODUCT
(
	 --PRODUCTID INT NOT NULL IDENTITY(1,1)
	 PRODUCTID INT NOT NULL
	,CODE VARCHAR(5)
	,[NAME] VARCHAR(100)
	,UNITPRICE NUMERIC(6,2) NOT NULL
	,CONSTRAINT PK_PRODUCTID PRIMARY KEY (PRODUCTID)
)
GO


CREATE TABLE CONTACT
(
	 CONTACTID VARCHAR(30) NOT NULL
	,CUSTOMERID INT NOT NULL
	,[ADDRESS] VARCHAR(150)
	,CITY VARCHAR(50)
	,PHONE VARCHAR(20)
	,CONSTRAINT PK_CONTACTID PRIMARY KEY (CONTACTID)
)
GO


CREATE TABLE CUSTOMERID
(
	 --CUSTOMERID INT NOT NULL IDENTITY(1,1)
	 CUSTOMERID INT NOT NULL
	,DRV_LIC_NO INT NOT NULL
	,CONSTRAINT PK_CUSTOMERID2 PRIMARY KEY (CUSTOMERID)
)
